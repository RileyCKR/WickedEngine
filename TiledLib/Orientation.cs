namespace TiledLib
{
    /// <summary>
    /// Defines the possible orientations for a Map.
    /// </summary>
    public enum Orientation : byte
    {
        /// <summary>
        /// The tiles of the map are orthogonal.
        /// </summary>
        Orthogonal,

        /// <summary>
        /// The tiles of the map are isometric.
        /// </summary>
        Isometric,
    }
}
